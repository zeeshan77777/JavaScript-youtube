const number = 5
switch (number) {
    case 1:
        console.log("number-1 macthed"); 
        break;
    case 2:
        console.log("number-2 macthed"); 
        break;
    case 3:
        console.log("number-3 macthed"); 
        break;
    case 4:
        console.log("number-4 macthed"); 
        break;
    case 5:
        console.log("number-5 macthed"); 
        break;

    default:
        console.log("No number matched");
        
        break;
}
